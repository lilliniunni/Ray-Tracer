class Light{
    contructor(){       
        this.r = 0;
        this.g = 0;
        this.b = 0;
        this.x = 0;
        this.y = 0;
        this.z = 0;
    }
    set setR(r){
        this.r = r;
    }
    set setG(g){
        this.g = g;
    }
    set setB(b){
        this.b = b;
    }
    
    set setX(x){
        this.x = x;
    }
    set setY(y){
        this.y = y;
    }
    set setZ(z){
        this.z = z;
    }
    
    get getR(){
        return this.r;
    }
    get getG(){
        return this.g;
    }
    get getB(){
        return this.b;
    }
    
    get getX(){
        return this.x;
    }
    get getY(){
        return this.y;
    }
    get getZ(){
        return this.z;
    }
}