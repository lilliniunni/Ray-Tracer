class Eye{
    contructor(){       
        this.x = 0;
        this.y = 0;
        this.z = 0;
    }
    set setX(x){
        this.x = x;
    }
    set setY(y){
        this.y = y;
    }
    set setZ(z){
        this.z = z;
    }
    
    get getX(){
        return this.x;
    }
    get getY(){
        return this.y;
    }
    get getZ(){
        return this.z;
    }
}