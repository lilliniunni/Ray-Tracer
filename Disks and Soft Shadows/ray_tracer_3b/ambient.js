class Ambient{
    constructor(){
        this.r = 0;
        this.g = 0;
        this.b = 0;
    }
    set setR(r){
        this.r = r;
    }
    set setG(g){
        this.g = g;
    }
    set setB(b){
        this.b = b;
    }
    get getR(){
        return this.r;
    }
    get getG(){
        return this.g;
    }
    get getB(){
        return this.b;
    }
}